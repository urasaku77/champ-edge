/// フォルムチェンジ可能なポケモンの定義（PC版 pokedata/exception.py の移植）。
///
/// ポケモンをタップしたときにフォルム/メガを切り替えてよいのは、ここに定義された
/// 図鑑番号だけ。地方フォルム（ガラル/ヒスイ等）やアルセウスのタイプ違いなどは
/// バトル中に変わらないので対象外（タップしても変化しない）。
library;

/// フォルムチェンジ可能なポケモンの図鑑番号。
/// バトル中フォルムチェンジ（ヒヒダルマ・メロエッタ・ギルガルド・ジガルデ・ヨワシ・
/// イルカマン・テラパゴス）＋ メガシンカ対応（form 11-12 を持つポケモン）。
const Set<int> kChangeableFormNos = {
  555, 648, 681, 718, 746, 964, 1024,
  3, 6, 9, 15, 18, 26, 36, 65, 71, 80, 94, 115, 121, 127, 130, 142,
  149, 154, 160, 181, 208, 212, 214, 227, 229, 248, 254, 257, 260,
  282, 302, 303, 306, 308, 310, 319, 323, 334, 354, 358, 359, 362,
  376, 398, 428, 445, 448, 460, 475, 478, 500, 530, 531, 545, 560,
  604, 609, 623, 652, 655, 658, 668, 670, 678, 687, 689, 691, 701,
  740, 780, 870, 952, 970,
};

/// バトル中フォルムチェンジの遷移（[no],[form] → 次のフォルム番号）。無ければ null。
/// PC版 get_next_form の移植。メガ以外（ヒヒダルマ等）の専用遷移を定義する。
int? nextBattleForm(int no, int form) {
  switch (no) {
    case 555: // ヒヒダルマ（通常⇄ダルマ / ガラル⇄ガラルダルマ）
      return const {0: 1, 1: 0, 2: 3, 3: 2}[form];
    case 648: // メロエッタ（ボイス⇄ステップ）
      return const {0: 1, 1: 0}[form];
    case 681: // ギルガルド（シールド⇄ブレード）
      return const {0: 1, 1: 0}[form];
    case 718: // ジガルデ（→パーフェクト）
      return const {0: 2, 1: 2}[form];
    case 746: // ヨワシ（単独⇄群れ）
      return const {0: 1, 1: 0}[form];
    case 964: // イルカマン（ナイーブ⇄マイティ）
      return const {0: 1, 1: 0}[form];
    case 1024: // テラパゴス（ノーマル→テラスタル→ステラ→テラスタル）
      return const {0: 1, 1: 2, 2: 1}[form];
    default:
      return null;
  }
}
